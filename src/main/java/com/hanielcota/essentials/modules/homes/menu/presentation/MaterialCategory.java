  private final List<Material> materials;

  MaterialCategory(@NonNull List<Material> materials) {
    this.materials = List.copyOf(materials);
  }

  /**
   * Returns the category that owns this material, or {@link #MISC} if none of the explicit lists
   * contain it.
   */
  public static @NonNull MaterialCategory of(@NonNull Material material) {
    for (var category : values()) {
      if (category != MISC && category.materials.contains(material)) {
        return category;
      }
    }
    return MISC;
  }

  public static @NonNull List<MaterialCategory> browsable() {
    return List.of(values());
  }

  public @NonNull List<Material> materials() {
    return materials;
  }

